1. 	
A sum of money at simple interest amounts to Rs. 815 in 3 years and to Rs. 854 in 4 years. The sum is:<br>

A.Rs. 650<br>
B.Rs. 690<br>
C.Rs. 698<br>
D.Rs. 700<br>
Answer: Option C<br>
Explanation:S.I. for 1 year = Rs. (854 - 815) = Rs. 39.<br>
S.I. for 3 years = Rs.(39 x 3) = Rs. 117.<br>
Principal = Rs. (815 - 117) = Rs. 698.<br></p>

