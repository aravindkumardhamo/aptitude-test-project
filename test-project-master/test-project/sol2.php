<h3>RATIO AND PROPORTION</h3><p>

1. Two numbers are respectively 20% and 50% more than a third number. The ratio of the two numbers is:<br>

A.	2 : 5<br>
B.	3 : 5<br>
C.	4 : 5<br>
D.	6 : 7<br>
Answer: Option C<br>

Explanation:

Let the third number be x.<br>

Then, first number = 120% of x =(120x/100)=(6x/5)<br>
Second number = 150% of x =(150x/100)=(3x/2)<br>
 Ratio of first two numbers =6x : 3x =12x : 15x = 4 : 5 </p>

<p>
2. A sum of money is to be distributed among A, B, C, D in the proportion of 5 : 2 : 4 : 3. If C gets Rs. 1000 more than D, what is B's share?<br>

A.Rs. 500<br>
B.Rs. 1500<br>
C.Rs. 2000<br>
D.None of these<br>
Answer: Option C<br>
Explanation:Let the shares of A, B, C and D be Rs. 5x, Rs. 2x, Rs. 4x and Rs. 3x respectively.<br>
Then, 4x - 3x = 1000<br>
x = 1000.<br>
B's share = Rs. 2x = Rs. (2 x 1000) = Rs. 2000.<br> </p>










































